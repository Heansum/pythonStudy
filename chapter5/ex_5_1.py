# 자료형 - 문자, 리스트, 튜플

simpleString = "파이썬"
simpleList = ["파", "이", "썬"]
simpleTuple = ("파", "이", "썬")

simpleString = "문자열"
simpleList = ["리", "스", "트"]
simpleTuple = ("튜", "플")

strToList = list(simpleString)
listToTuple = tuple(simpleList)
tupleToStr = str(simpleTuple)

print("str -> list =", strToList)
print("list -> tuple =", listToTuple)
print("tuple -> str =", tupleToStr)


