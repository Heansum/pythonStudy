# Tuple 덧셈
oddTuple = (1, 3, 5, 7, 9)
evenTuple = 2, 4, 6, 8, 10

fullTuple = oddTuple + evenTuple

print(fullTuple)

# Tuple 곱셈
multiTuple = (1,) # 요소가 하나인 튜플은 ( ,)의 형식으로 만들어야함

print(multiTuple * 5)

dashTuple = ("=", ) * 3
asteriskTuple = ("*",) * 3
fullTuple = dashTuple + asteriskTuple

print(fullTuple)
